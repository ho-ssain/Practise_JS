// Rules for naming variables.
/* An identifier or keyword cannot immediately follow a numeric literal
Example-:
  var 1value; // Error!!!
  var value1  // valid!!!

can use only underscore( _ ) or dollar sign from special sign.
Example:
  var first_name // valid
  var _firstName // valid

  var first$Name // valid
  var $firstname // valid

can't use spaces
Example:
  var f name // invalid


  var first_name // snake case writing
  var firstName // camel case writing

convention:
  1. variable should be start lower case letter
  2. use camal case writing
*/

var value1 = 3;
console.log("Add: " + (value1 + 10));
console.log("Sub: " + (value1 - 1));
console.log("Mul: " + (value1 * 2));
console.log("Div: " + (value1 / 1));
console.log("Pow: " + (value1 ** 3));
console.log("Mol: " + (value1 % 2));
